<?php

namespace frontend\controllers;

use backend\components\Data;
use backend\components\Helper;
use common\models\User;
use frontend\models\SignupForm;
use Yii;
use yii\web\Controller;
use frontend\models\ContactForm;

/**
 * Site controller
 */
class ProfileController extends Controller
{

    public function beforeAction($action)
    {
        if (Yii::$app->user->isGuest) {
            return $this->goHome();
        }
        $this->layout = 'profile';
        return parent::beforeAction($action); // TODO: Change the autogenerated stub
    }


    /**
     * Displays homepage.
     *
     * @return mixed
     */
    public function actionIndex()
    {

        if (Yii::$app->request->post('User')) {
            User::SaveUserData(Yii::$app->request->post('User'));
        }
        $model = User::findIdentity(Yii::$app->user->getId());
        return $this->render('index', [
            'model' => $model,
        ]);
    }

    public function actionDrag()
    {
//        $this->layout = false;
        return $this->render('drag', [

        ]);
    }

    public function actionChoose()
    {
        $this->layout = false;
        return $this->render('choose', [

        ]);
    }
    public function actionChoose2()
    {
        $this->layout = false;
        return $this->render('choose_2', [

        ]);
    }
    public function actionContact()
    {
        $model = new ContactForm();
        if ($model->load(Yii::$app->request->post()) && $model->validate()) {
            if ($model->sendEmail(Yii::$app->params['adminEmail'])) {
                exit;
                Yii::$app->session->setFlash('success', 'Thank you for contacting us. We will respond to you as soon as possible.');
            } else {
                Yii::$app->session->setFlash('error', 'There was an error sending your message.');
            }

            return $this->refresh();
        } else {
            return $this->render('contact', [
                'model' => $model,
            ]);
        }
    }

    public function actionAbout()
    {
        return $this->render('about');
    }

    public function actionFaq()
    {
        return $this->render('faq');
    }


}
